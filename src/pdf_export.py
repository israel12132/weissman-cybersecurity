def _sanitize_filename(s: str, max_len: int = 80) -> str:
    """Safe filename: alphanumeric, dash, underscore only."""
    if not s:
        return "report"
    out = "".join(c if c.isalnum() or c in "-_" else "_" for c in str(s))
    return out[:max_len] or "report"


def generate_report_pdf_auto(
    run_id: int,
    run_created_str: str,
    findings: list[dict[str, Any]],
    summary: dict[str, Any],
    client_id_to_name: dict[str, str],
    tech_stack: list[str] | None = None,
    client_targets: dict[str, str] | None = None,
) -> Path | None:
    """
    Generate PDF for verified findings and return path. Naming: [CompanyName]_[Vulnerability_ID]_[Timestamp].pdf.
    Saves under project reports/ directory. All data from DB (no mock). client_targets = client_id -> base URL for CURL.
    """
    if not findings:
        return None
    base = Path(__file__).resolve().parent.parent
    reports_dir = base / "reports"
    reports_dir.mkdir(parents=True, exist_ok=True)
    first = findings[0]
    client_id = str(first.get("client_id") or "")
    company_name = (client_id_to_name.get(client_id) or client_id or "Client").strip()
    if not company_name:
        company_name = "MultiClient"
    f = (first.get("finding") or {})
    vuln_id = str(f.get("id") or f.get("source_id") or f.get("title") or run_id)
    vuln_id = _sanitize_filename(vuln_id, 64)
    company_name = _sanitize_filename(company_name, 40)
    ts = run_created_str.replace(" ", "_").replace(":", "-").replace(".", "-")[:19]
    ts = "".join(c if c.isalnum() or c in "-_" else "_" for c in ts)
    filename = f"{company_name}_{vuln_id}_{ts}.pdf"
    output_path = reports_dir / filename
    path = generate_report_pdf(
        findings,
        summary,
        run_id,
        run_created_str,
        target_name=company_name,
        output_path=output_path,
        tech_stack=tech_stack,
        client_targets=client_targets,
    )
    if path and path.exists():
        return path
    return None
