def weissman_security_score(by_severity: dict[str, int]) -> int:
    """
    Weissman Security Rating: base 100, deduct -25 Critical, -15 High, -5 Medium. Minimum 0.
    """
    score = 100
    score -= 25 * (by_severity.get("critical") or 0)
    score -= 15 * (by_severity.get("high") or 0)
    score -= 5 * (by_severity.get("medium") or 0)
    return max(0, score)


def score_color_class(score: int) -> str:
    """Green 90-100, Yellow 70-89, Red below 70."""
    if score >= 90:
        return "score-green"
    if score >= 70:
        return "score-yellow"
    return "score-red"


def cvss_score_from_anomaly(anomaly_type: str, severity: str = "") -> float:
    """CVSS-like 1-10 from anomaly type and severity."""
    a = (anomaly_type or "").lower()
    s = (severity or "").lower()
    if "500" in a or "crash" in a or "internal server error" in a:
        return 8.5
    if "time" in a or "latency" in a or "dos" in a:
        return 6.5
    if "content-length" in a or "length" in a:
        return 5.0
    if s in ("critical", "high"):
        return 7.5
    if s == "medium":
        return 5.5
    if s == "low":
        return 3.5
    return 5.0


def remediation_for_anomaly(anomaly_type: str) -> str:
    """Suggest a fix for the discovered vulnerability."""
    a = (anomaly_type or "").lower()
    if "500" in a or "crash" in a:
        return (
            "Implement input validation and sanitization for all user-controlled fields. "
            "Ensure the application does not expose stack traces to clients. "
            "Add proper exception handling and return generic error responses."
        )
    if "time" in a or "latency" in a:
        return (
            "Add server-side timeouts and rate limiting. Investigate blind injection or "
            "resource exhaustion. Implement query/request time bounds and circuit breakers."
        )
    if "length" in a or "content" in a:
        return (
            "Enforce maximum input length and validate payload size. Implement input sanitization "
            "for the affected field to prevent buffer or memory-related issues."
        )
    return (
        "Implement input sanitization for the affected endpoint. Validate and constrain "
        "all user inputs; avoid passing unsanitized data to interpreters or system calls."
    )


def _escape(s: str) -> str:
    return html.escape(str(s) if s is not None else "")


def _severity_to_impact(sev: str) -> int:
    """Map severity to Impact 1-5 for heatmap."""
    s = (sev or "").lower()
    if s == "critical":
        return 5
    if s == "high":
        return 4
    if s == "medium":
        return 3
    if s == "low":
        return 2
    return 1


EXPLOIT_DB_LINK = "https://www.exploit-db.com/"


def _reproduction_curl(
    finding: dict,
    target_base_url: str,
    proof: str | None = None,
) -> str:
    """
    Generate a valid, executable reproduction command. Never returns <target> or <payload>.
    If no specific PoC: "Manual verification required via [Exploit-DB link]".
    """
    if proof and proof.strip():
        proof_first = proof.strip().split("\n")[0][:400]
        return proof_first if proof_first else f"Manual verification required via {EXPLOIT_DB_LINK}"
    target = (target_base_url or "").strip()
    if not target:
        return f"Manual verification required via {EXPLOIT_DB_LINK}"
    if not target.startswith(("http://", "https://")):
        target = f"https://{target}"
    f_type = (finding.get("type") or "").lower()
    desc = (finding.get("description") or "").lower()
    title = (finding.get("title") or "").lower()
    # CRLF injection
    if "crlf" in f_type or "crlf" in desc or "crlf" in title or "header injection" in desc:
        return f'curl -I "{target}/?param=%0d%0aSet-Cookie:Weissman-Test=1"'
    # Directory traversal
    if "traversal" in desc or "path traversal" in desc or "directory" in desc or ".." in title:
        return f'curl "{target}/../../etc/passwd"'
    # SQL injection (safe check)
    if "sql" in f_type or "sql" in desc or "injection" in desc:
        return f'curl "{target}/?id=1%27"'
    # XSS
    if "xss" in f_type or "xss" in desc or "script" in desc:
        return f'curl -G "{target}/" --data-urlencode "q=<script>alert(1)</script>"'
    # Fuzzer / 500 / time anomaly (use POST with minimal payload)
    if "500" in desc or "time" in desc or "latency" in desc or "anomaly" in desc:
        payload_esc = '{"test":"Weissman"}'
        return f'curl -X POST "{target}" -H "Content-Type: application/json" -d \'{payload_esc}\''
    # Generic CVE: no specific PoC
    return f"Manual verification required via {EXPLOIT_DB_LINK}"


def _reproduction_snippets_multilang(
    finding: dict,
    target_base_url: str,
    proof_curl: str | None,
) -> dict[str, str]:
    """Return Bash (curl), Python (requests), JavaScript (fetch), Go snippets. No fake payloads: when no real proof, return Manual verification only."""
    manual_msg = "Manual verification required. Use Exploit-DB or vendor advisory for PoC."
    if not proof_curl or proof_curl.strip().startswith("Manual"):
        return {"bash": manual_msg, "python": manual_msg, "javascript": manual_msg, "go": manual_msg}
    target = (target_base_url or "").strip()
    if not target.startswith(("http://", "https://")):
        target = f"https://{target}" if target else "https://TARGET_HOST/"
    payload: str | None = None
    if "curl" in proof_curl and " -d " in proof_curl:
        m = re.search(r"-d\s+['\"]([^'\"]+)['\"]", proof_curl)
        if m:
            payload = m.group(1).replace("\\\"", '"')
    if not payload:
        return {"bash": proof_curl, "python": manual_msg, "javascript": manual_msg, "go": manual_msg}
    bash_s = proof_curl
    python_s = f'''import requests\nresp = requests.post("{target}", json={payload!r}, timeout=10)\nprint(resp.status_code, len(resp.content))'''
    js_s = f'''fetch("{target}", {{ method: "POST", headers: {{ "Content-Type": "application/json" }}, body: JSON.stringify({payload}) }})\n  .then(r => r.text()).then(console.log)'''
    go_s = f'''resp, _ := http.Post("{target}", "application/json", strings.NewReader(`{payload}`))\ndefer resp.Body.Close()\n// Check resp.StatusCode, resp.Body'''
    return {"bash": bash_s, "python": python_s, "javascript": js_s, "go": go_s}


def _build_heatmap_matrix(findings: list[dict[str, Any]]) -> list[list[int]]:
    """5x5 matrix: rows = Impact (1-5), cols = Likelihood (1-5). Default Likelihood=3."""
    matrix = [[0] * 5 for _ in range(5)]
    for item in findings:
        f = item.get("finding") or {}
        sev = (f.get("severity") or "info").lower()
        impact = _severity_to_impact(sev)
        likelihood = 3  # default
        if sev in ("critical", "high"):
            likelihood = 4
        elif sev in ("low", "info"):
            likelihood = 2
        # matrix[impact-1][likelihood-1]
        matrix[5 - impact][likelihood - 1] += 1
    return matrix


